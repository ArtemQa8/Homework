package model

// С Большой буквы, значит можем экспортировать
type Доска struct {
	строки  uint
	столбцы uint
	клетки  [][]*Фигура // Это двухмерный слайс указателей на фигуры (скоро будет лекция по ним)
	// пустые клетки означают, что размер будет динамический
	// используем указатель, чтобы работать с оригиналом
}

// Создаем доску заданного размера, расставляем фигуры (цикл и метод)
// Указатель, чтобы работать с оригиналом, запомни!
func НоваяДоска(новСтроки, новСтолбцы uint) *Доска {
	д := &Доска{строки: новСтроки, столбцы: новСтолбцы}
	д.клетки = make([][]*Фигура, новСтроки)
	for i := range д.клетки {
		д.клетки[i] = make([]*Фигура, новСтолбцы)
	}

	// Пешки
	for столбец := uint(0); столбец < новСтолбцы; столбец++ {
		д.клетки[1][столбец] = НоваяФигура(Белые, Пешка)
		д.клетки[новСтроки-2][столбец] = НоваяФигура(Чёрные, Пешка)
	}

	// Белые и чёрные фигуры
	д.расставитьФигуры(0, Белые)
	д.расставитьФигуры(новСтроки-1, Чёрные)

	return д
}

// приватный метод для расстановки фигур
func (д *Доска) расставитьФигуры(строка uint, цвет ЦветФигуры) {
	с := д.столбцы
	д.клетки[строка][0] = НоваяФигура(цвет, Ладья)
	д.клетки[строка][с-1] = НоваяФигура(цвет, Ладья)
	д.клетки[строка][1] = НоваяФигура(цвет, Конь)
	д.клетки[строка][с-2] = НоваяФигура(цвет, Конь)
	д.клетки[строка][2] = НоваяФигура(цвет, Слон)
	д.клетки[строка][с-3] = НоваяФигура(цвет, Слон)
	д.клетки[строка][с/2-1] = НоваяФигура(цвет, Ферзь)
	д.клетки[строка][с/2] = НоваяФигура(цвет, Король)
}

// Глобальный метод, возвращает указатель на фигуру или nil если клетка пустая
func (д *Доска) ФигураНа(строка, столбец uint) *Фигура {
	return д.клетки[строка][столбец]
}

// Публичный метод для перемещения фигур
func (д *Доска) УстановитьФигуру(строка, столбец uint, фигура *Фигура) {
	д.клетки[строка][столбец] = фигура
}

// Глобальный метод для раскраски
func (д *Доска) ОтобразитьКлетку(строка, столбец uint) string {
	светлая := (строка+столбец)%2 == 0
	фон := BlackSqBg
	if светлая {
		фон = WhiteSqBg
	}
	ф := д.клетки[строка][столбец]
	if ф == nil {
		return фон + "   " + Reset
	}
	return ф.Строка(строка, столбец)
}

// Строки возвращает количество строк
func (д *Доска) Строки() uint { return д.строки }

// Столбцы возвращает количество столбцов
func (д *Доска) Столбцы() uint { return д.столбцы }
