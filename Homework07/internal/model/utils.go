func abs(x int) int {
	if x < 0 {
		return -x
	}
	return x
}

func знак(x int) int {
	if x > 0 {
		return 1
	} else if x < 0 {
		return -1
	}
	return 0
}

func центрирование(имя string, ширинаДоски int) string {
	длина := utf8.RuneCountInString(имя)
	всегоПробелов := ширинаДоски - длина
	if всегоПробелов <= 0 {
		return имя
	}
	левый := всегоПробелов / 2
	правый := всегоПробелов - левый
	return strings.Repeat(" ", левый) + имя + strings.Repeat(" ", правый)
}

func имяКолонки(индекс int) string {
	индекс++
	var результат []byte
	for индекс > 0 {
		индекс--
		остаток := индекс % 26
		результат = append([]byte{byte('A' + остаток)}, результат...)
		индекс /= 26
	}
	return string(результат)
}

func ФорматироватьХод(ход Ход) string {
	// рокировка
	if ход.ХодившаяФигура != nil && ход.ХодившаяФигура.Тип() == Король &&
		abs(ход.ВСтолбец-ход.ОтСтолбец) == 2 {
		строка := "O-O"
		if ход.ВСтолбец < ход.ОтСтолбец {
			строка = "O-O-O"
		}
		if ход.Мат {
			строка += "#"
		} else if ход.Шах {
			строка += "+"
		}
		return строка
	}

	символ := ""
	if ход.ХодившаяФигура != nil && ход.ХодившаяФигура.Тип() != Пешка {
		символ = ход.ХодившаяФигура.Символ() + " "
	}

	от := КлеткаВСтроку(ход.ОтСтрока, ход.ОтСтолбец)
	в := КлеткаВСтроку(ход.ВСтрока, ход.ВСтолбец)

	разделитель := "-"
	if ход.Съедена != nil {
		разделитель = "x"
	}

	строка := символ + от + разделитель + в
	if ход.Мат {
		строка += "#"
	} else if ход.Шах {
		строка += "+"
	}
	return строка
}

func КлеткаВСтроку(строка, столбец int) string {
	return имяКолонки(столбец) + fmt.Sprintf("%d", строка+1)
}

