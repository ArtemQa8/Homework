// СделатьАвтоход находит случайный допустимый ход для текущего игрока,
// делает паузу 2-4 секунды и возвращает этот ход.
func СделатьАвтоход(игра *model.Игра) (*model.Ход, error) {
	цвет := игра.ТекущийЦвет()
	строки := игра.Доска().Строки()
	столбцы := игра.Доска().Столбцы()

	var возможные []*model.Ход

	for отСтр := 0; отСтр < строки; отСтр++ {
		for отСтл := 0; отСтл < столбцы; отСтл++ {
			фигура := игра.Доска().ФигураНа(отСтр, отСтл)
			if фигура == nil || фигура.Цвет() != цвет {
				continue
			}
			правила := model.ПравилаДля(фигура)
			if правила == nil {
				continue
			}
			for вСтр := 0; вСтр < строки; вСтр++ {
				for вСтл := 0; вСтл < столбцы; вСтл++ {
					if отСтр == вСтр && отСтл == вСтл {
						continue
					}
					ход := model.НовыйХод(отСтр, отСтл, вСтр, вСтл)
					if правила.МожетХодить(ход, игра.Доска()) && игра.ЛегальныйХод(ход, цвет) {
						возможные = append(возможные, ход)
					}
				}
			}
		}
	}

	if len(возможные) == 0 {
		return nil, fmt.Errorf("нет допустимых ходов")
	}

	задержка := time.Duration(2000+rand.Intn(2000)) * time.Millisecond
	time.Sleep(задержка)

	return возможные[rand.Intn(len(возможные))], nil
}

// СохранитьИгроков добавляет игроков в хранилище (вызывается один раз).
func СохранитьИгроков(игра *model.Игра, хранилище *repository.Хранилище) {
	хранилище.Добавить(игра.Игрок1())
	хранилище.Добавить(игра.Игрок2())
}

// СохранитьИгру добавляет текущую игру в хранилище (вызывается один раз).
func СохранитьИгру(игра *model.Игра, хранилище *repository.Хранилище) {
	хранилище.Добавить(игра)
}

// СохранитьХод добавляет последний ход в хранилище.
func СохранитьХод(игра *model.Игра, хранилище *repository.Хранилище) {
	ходы := игра.Ходы()
	if len(ходы) > 0 {
		последний := ходы[len(ходы)-1]
		хранилище.Добавить(последний)
	}
}
