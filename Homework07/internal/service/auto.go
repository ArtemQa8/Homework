package service

import (
	"fmt"
	"math/rand"
	"time"

	"mod.go/internal/model"
)

func СделатьАвтоход(игра *model.Игра) (*model.Ход, error) {
	цвет := игра.ТекущийЦвет()
	строки := игра.Доска().Строки()
	столбцы := игра.Доска().Столбцы()

	var возможные []*model.Ход

	for отСтр := 0; отСтр < строки; отСтр++ {
		for отСтл := 0; отСтл < столбцы; отСтл++ {
			фигура := игра.Доска().ФигураНа(отСтр, отСтл)
			if фигура == nil || фигура.Цвет() != цвет {
				continue
			}
			правила := model.ПравилаДля(фигура)
			if правила == nil {
				continue
			}
			for вСтр := 0; вСтр < строки; вСтр++ {
				for вСтл := 0; вСтл < столбцы; вСтл++ {
					if отСтр == вСтр && отСтл == вСтл {
						continue
					}
					ход := model.НовыйХод(отСтр, отСтл, вСтр, вСтл)
					if правила.МожетХодить(ход, игра.Доска()) && игра.ЛегальныйХод(ход, цвет) {
						возможные = append(возможные, ход)
					}
				}
			}
		}
	}

	if len(возможные) == 0 {
		return nil, fmt.Errorf("нет допустимых ходов")
	}

	задержка := time.Duration(2000+rand.Intn(2000)) * time.Millisecond
	time.Sleep(задержка)

	return возможные[rand.Intn(len(возможные))], nil
}

func СохранитьИгроков(игра *model.Игра, шина chan<- model.ОбъектХранилища) {
	шина <- игра.Игрок1()
	шина <- игра.Игрок2()
}

func СохранитьИгру(игра *model.Игра, шина chan<- model.ОбъектХранилища) {
	шина <- игра
}

func СохранитьХод(игра *model.Игра, шина chan<- model.ОбъектХранилища) {
	ходы := игра.Ходы()
	if len(ходы) > 0 {
		последний := ходы[len(ходы)-1]
		шина <- последний
	}
}

func СимулироватьПартию(сим *model.СостояниеПартии, шина chan<- model.ОбъектХранилища, остановить <-chan struct{}) {
	defer func() {
		сим.Завершена = true
	}()

	for {
		select {
		case <-остановить:
			сим.ПоследнийХод = "Симуляция остановлена"
			return
		default:
		}

		if сим.Игра.Мат(сим.Игра.ТекущийЦвет()) {
			сим.ПоследнийХод = "Мат!"
			return
		}

		цвет := сим.Игра.ТекущийЦвет()
		var имя string
		if цвет == model.Белые {
			имя = сим.Игра.Игрок1().Имя()
		} else {
			имя = сим.Игра.Игрок2().Имя()
		}

		начало := time.Now()

		ход, err := СделатьАвтоход(сим.Игра)
		if err != nil {
			сим.ПоследнийХод = fmt.Sprintf("Ошибка: %v", err)
			return
		}
		if err := сим.Игра.СделатьХод(ход); err != nil {
			сим.ПоследнийХод = fmt.Sprintf("Ошибка: %v", err)
			return
		}
		СохранитьХод(сим.Игра, шина)

		затрачено := time.Since(начало)
		сим.ПоследнийХод = fmt.Sprintf("%s (%s)", имя, model.ФорматироватьХод(*ход))
		сим.ВремяХода = затрачено

		select {
		case <-остановить:
			сим.ПоследнийХод = "Симуляция остановлена"
			return
		case <-time.After(1 * time.Second):
		}
	}
}
